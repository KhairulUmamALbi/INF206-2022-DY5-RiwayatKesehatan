                        <p class="text-start"> Kunyit terkenal sebagai bahan untuk memasak, selain itu kunyit juga
                            bisa dipakai untuk pengobatan tradisioanl. Hal tersebut berkat kandungan kurkumin yang ada
                            pada rimpang kunyit. Selain memberikan warna pada kunyit, kurkumin juga dipercaya mempunyai
                            manfaat yang sangat baik bagi kesehatan tubuh.</p>

                        <p class="text-start">
                            <b>Merangkum darialodokter dan halodoc, berikut ini beberapa manfaat kunyit untuk kesehatan
                                tubuh:</b>
                        </p>

                        <p class="text-start">
                            <b>1. Meringankan gangguan menstruasi</b>
                        </p>

                        <p class="text-start">Sebuah penelitian mengungkapkan bahwa suplemen yang terkandung
                            kurkumin didalamnya bisamembantu untuk meredakan gejala sindrom pramenstruasi (PMS). Kunyit
                            juga bisa membantu meringankan kram yang biasanya terjadi dan diaai oleh wanita selama
                            datang bulan atau menstruasi.</p>

                        <p class="text-start"><b>2. Merawat dan menjaga kesehatan paru</b></p>

                        <p class="text-start">Kunyit memiliki sifat antiinflamasi dan antioksidan yang bisa membantu
                            dalam mengurangi gejala yang berhubungan dengan masalah kesehatan pada paru kronis atau
                            jangka panjang. Kunyit juga efektif dalam mengobati penyakit asma, cedera pada paru, kanker,
                            dan fibrosis dan kistik.</p>

                        <p class="text-start"><b>3. Mencegah penyakit jantung</b></p>

                        <p class="text-start">Penyakit jantung merupakan salah satu pemicu kematian nomor wahid di
                            dunia. Kunyit dipercaya bisa membantu dalam mencegah penyakit jantung berkat kandungan
                            kurkumin didalamnya. Kurkumin juga bisa membantu untuk meningkatkan fungsi endotelium atau
                            lapisan yang ada di dalam pembuluh darah.</p>

                        <p class="text-start"><b>4. Melindungi organ hati</b></p>

                        <p class="text-start">Kunyit juga bisa membantu dalam melindungai dan juga menjaga kesehatan
                            organ hati. Kunyit juga dapat membantu dalam meningkatkan produksi cairan yang ada dalam
                            empedu pada saluran pencernaan, serta dapat membantu dalam melindungi sel yang terdapat
                            dalam organ hati agar tidak rusak akibat paparan senaywa yang berhubungan dengan empedu.</p>

                        <p class="text-start"><b>5. Mengurangi depresi</b></p>

                        <p class="text-start">Ada sebuah penelitian menyebutkan bahwa dengan mnengonsumsi kunyit
                            dapat meredakan gejala depresi. Hal tersebut disebabkan oleh adanya kandungan kurkumin yang
                            terdapat dalam kunyit. Kandungan tersebut bisa membantu antidepresan bekerja lebih baik.</p>

                        <p class="text-start">Selain manfaat kunyit yang telah disebutkan tadi, masih ada lagi loh
                            manfaat yang bisa kamu dapatkan dari kunyit ini, seperti menurunkan kolesterol jahat,
                            meringankan gejala asam urat, menjaga kesehatan mata. Nah, sekarang kalian sudah tau kan
                            manfaat kunyit yang sangat luar bisa bagi kesehatan, semoga bermanfaat.</p>


                        </p>
