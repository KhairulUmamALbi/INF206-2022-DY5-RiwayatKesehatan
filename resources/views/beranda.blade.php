                        <p class="text-start"> Jumlah orang yang beralih ke pola makan nabati meningkat di seluruh
                            dunia. Hal ini membuat orang tua yang menjalani pola makan tersebut juga mendorong anak-anak
                            mereka untuk melakukan hal yang sama. Namun, beberapa penelitian telah mengevaluasi dampak
                            diet vegetarian pada pertumbuhan anak dan status gizi. Apakah anak-anak yang makan daging
                            mengalami pertumbuhan yang lebih baik daripada mereka yang diet vegetarian?</p>

                        <p class="text-start">Sebuah studi baru yang terbit di jurnal Pediatrics menemukan anak yang
                            diet vegetarian memiliki ukuran pertumbuhan dan nutrisi yang sama dengan anak yang
                            mengonsumsi makanan non-vegetarian.</p>

                        <p class="text-start">
                            Hal itu menggarisbawahi perlunya memikirkan ulang untuk mendorong anak-anak dengan berat
                            badan di bawah indeks massa tubuh (IMT) melakukan diet vegetarian, lapor The Health Site.
                        </p>

                        <p class="text-start">
                            Peneliti dari St. Michael's Hospital of Unity Health Toronto mengatakan dalam studi ini
                            bahwa kekurangan berat badan merupakan indikator kekurangan gizi.
                        </p>

                        <p class="text-start">
                            "Mungkin itu juga merupakan tanda bahwa kualitas makanan tidak memenuhi kebutuhan gizi anak
                            untuk mendukung pertumbuhan normal," tulis peneliti.
                        </p>
                        <p class="text-start">
                            Oleh karenanya, untuk anak-anak yang mengikuti diet vegetarian, para peneliti menekankan
                            perlunya akses ke ahli gizi yang dapat memantau pertumbuhan, edukasi, dan bimbingan demi
                            mendukung perkembangan dan nutrisi mereka.
                        </p>

