                        <h2 class="text-center">
                            <!-- Judul --> Studi Temukan Tidur Bisa Jadi 'Kunci' Penurunan Berat Badan
                        </h2><br>
                        <p class="text-start"> Bukan rahasia lagi, tidur punya peran penting untuk kesehatan. Studi
                            terbaru bahkan menemukan kualitas tidur juga menentukan kemampuan seseorang untuk menurunkan
                            berat badan.
                            Melansir <a
                                href="https://www.medicalnewstoday.com/articles/getting-enough-quality-sleep-may-be-the-key-to-weight-loss#Low-calorie-diet-increases-sleep-duration">Medical
                                News Today<a />, sebuah penelitian yang dilakukan para peneliti dari Universitas
                                Kopenhagen menunjukkan, kualitas tidur yang buruk dan terbatas meningkatkan risiko
                                gangguan metabolisme, penambahan berat badan, dan obesitas.</p>

                        <p class="text-start">Kurangnya kualitas tidur juga terbukti meningkatkan keinginan untuk
                            mengonsumsi makanan berkalori dan mengandung karbohidrat tinggi yang terkait dengan
                            penambahan berat badan. Penelitian itu menemukan bahwa kurang tidur yang berkualitas juga
                            dapat merusak upaya orang untuk mempertahankan penurunan berat badan setelah diet.
                            Penelitian ini menggunakan data dari uji coba terkontrol plasebo acak S-LiTE. Para peneliti
                            mempelajari kualitas dan durasi tidur pada 195 orang dewasa dengan obesitas. Para peserta
                            mengikuti diet rendah kalori selama 8 minggu dan kehilangan rata-rata 12 persen berat badan.
                        </p>

                        <p class="text-start"> Jane Odgen yang tidak terlibat dalam penelitian ini mengatakan, tidur
                            memang berkaitan dengan berat badan. Entah itu kurang tidur bisa menjadi penyebab penambahan
                            berat badan atau berat badan menyebabkan kurang tidur.

                            Dia juga menanggapi hasil penelitian tersebut. Menurutnya, bagian pertama dari penelitian
                            ini menunjukkan penurunan berat badan terkait dengan meningkatnya kualitas tidur. Namun,
                            tidak ada kelompok kontrol dan pengacakan dalam bagian ini.</p>

                        <p class="text-start">"Jadi itu mungkin bukan penurunan berat badan dan bisa menjadi sesuatu
                            yang lain, seperti waktu, sedang belajar, atau makan lebih banyak buah dan sayuran, terlepas
                            dari penurunan berat badan," kata dia.</p>

                        <p class="text-start">Studi jangka panjang lainnya juga menunjukkan bahwa orang dewasa
                            dengan obesitas yang tidur kurang dari enam jam semalam mengalami peningkatan indeks massa
                            tubuh (BMI) sebesar 1,1 kilogram/m2.

                            Sebagai perbandingan, orang dewasa dengan berat badan berlebih yang mencapai kualitas tidur
                            lebih baik dapat menurunkan BMI hingga 0,16 kilogram/m2.</p>
