                        <h2 class="text-center">
                            <!-- Judul --> Manfaat Air Kelapa untuk Kesehatan, Salah Satunya Mencegah Dehidrasi
                        </h2><br>
                        <div class="f1">
                            <author>Farida Hasanatul</author>
                        </div>
                        <div class="fr">
                            <time itemprop="datePublished" datetime="06 May 2022 | 09:00">Jum'at, 06 Mei 2022 | 09:00
                                WIB</time>
                        </div>
                        <p class="text-start"> Kalian pastinya telah mengetahui bahwa air kelapa memiliki segudang
                            manfaat bagi kesehatan tubuh. Tidak hanya mampu menghilangkan dahaga kala cuaca tengah
                            terik, air kelapa juga bisa membantu meningkatkan elektrolit dan juga mineral yang ada dalam
                            tubuh. Bahkan di dalam air kelapa terkandung nutrisi yang dapat membantu dala menghalau
                            kerusakan yang ada pada sel dalam tubuh seperti antioksidan.</p>

                        <p class="text-start">Merangkum dari alodokter, hellosehat, dan klikdokter, berikut ini 4
                            manfaat yang bisa kamu dapatkan dengan mengonsumsi air kelapa:
                        </p>

                        <p class="text-start">
                            <b>1. Meningkatkan stamina tubuh</b>
                        </p>

                        <p class="text-start">Selain memberikan sensasi menyegarkan, air kelapa juga bisa membantu
                            untuk meningkatkan stamina pad tubuh loh. hal tersebut dikarenakan adanya karbohidrat yang
                            terkandung di dalam air kelapa, yang digunakan oleh tubuh dalam menghasilkan sebuah energi,
                            sehingga bisa membuatmu lebih bersemangat lagi dalam menjalankan kegiatan sehari-hari.
                        </p>
                        <p class="text-start">
                            <b>2. Mengatur tekanan darah dan kolestrol</b>
                        </p>

                        <p class="text-start">Mengonsumsi air kelapa juga sangat bagus untuk memelihara kesehatan
                            jantung, karena bisa membantu dalam meningkatkan sirkulasi pada darah, mengurangi risiko
                            jantung dan stroke, serta memapu untuk menurunkan tekanan darah tinggi.</p>

                        <p class="text-start">Hal tersebut berkat adanya kandungan kalium yang cukup tinggi serta
                            natrium yang rendah didalam air kelapa.
                        </p>
                        <p class="text-start">
                            Natrium berperan penting dalam menurunkan kadar gula darah pada seseorang yang mempunyai
                            tekanan darah tinggi. Sehingga air kelapa memiliki khasiat untuk mencegah terbentuknya
                            sebuah penggumpalan darah.
                        </p>

                        <p class="text-start">
                            <b>3. Mencegah dehidrasi</b>
                        </p>
                        <p class="text-start">
                            Ternyata kandungan yang terdapat di dalam air kelapa lebih ampuh dalam mencegah dehidrasi
                            dibadingkan dengan minuman energi atau minuman isotonik. </p>

                        <p class="text-start">Di dalam air kelapa terdapat kandungan kalium sebesar 61 miligram, dan
                            gula alami sebesar 1,3 gram di setiap 200 mililiternya. Sedangkan di dalam minuman energi
                            dan isotonik hanya terdapat kandungan kalium setengahnya saja, serta terdapat jumlah pemanis
                            bautan lima kali lipat dan pastinya mempunyai dampak buruk bagi kesehatan.
                        </p>

                        <p class="text-start">
                            <b>4. Kaya gizi</b>
                        </p>

                        <p class="text-start">Terdapat 5 jenis elektrolit esensial bagi tubuh manusia di dalam air
                            kelapa, seperti agnesium, kalsium, natrium, kalium dan fosfor. Berkat kandungan tersebut,
                            khasiat air kelapa dapat dinikmati oleh para penderita penyakit apapun.
                        </p>
