                        <p class="text-center">
                            <!-- Isi --> Pecinta kopi hitam alias espresso wajib mewaspadai kenaikan kadar kolesterol
                            dalam darah. Kok bisa?
                        </p>

                        <p class="text-start">Risiko kolesterol tinggi akibat kebanyakan minum espresso dibuktikan
                            oleh sebuah studi yang terbit di jurnal BMJ Open Heart. Dalam studi itu, para peneliti dari
                            Arctic University of Norway, University of Oslo dan University of Gothenburg di Swedia
                            mempelajari data survei 21.083 orang Norwegia, berusia 40 tahun ke atas, lalu membandingkan
                            kebiasaan minum kopi dengan kadar kolesterol melalui sampel darah. Mereka ingin menemukan
                            bagaimana berbagai jenis kopi dapat mempengaruhi kadar kolesterol.</p>
                        <p class="text-start">Peneliti menemukan orang yang minum tiga sampai lima cangkir espresso
                            per hari memiliki kadar kolesterol tinggi, dibandingkan dengan mereka yang tidak minum
                            espresso. Menurut data, hubungan antara espresso dan kolesterol lebih kuat untuk pria
                            daripada wanita </p>

                        <p class="text-start">
                            Temuan lainnya, mereka yang minum enam atau lebih cangkir kopi saring setiap hari dikaitkan
                            dengan kadar kolesterol yang lebih tinggi pada wanita, tetapi tidak pada pria. Hasil studi
                            ini menunjukkan jenis kopi yang seseorang minum berbeda dampaknya bagi kesehatan. Studi
                            sebelumnya memperlihatkan kopi yang disaring mungkin lebih baik untuk kesehatan jantung,
                            karena espresso tanpa filter mengandung lebih banyak senyawa spesifik dinamakan kafestol dan
                            kahweol yang terkait dengan kolesterol tinggi. Walau begitu, kopi mengandung ribuan senyawa
                            dan banyak yang memilik dampak positif bagi kesehatan sehingga diperlukan lebih banyak
                            penelitian untuk menentukan cara paling sehat untuk menikmatinya.
                        </p>
                        <p class="text-start">
                            Menurut studi, minum kopi hingga lima cangkir sehari umumnya aman dan mungkin memiliki
                            manfaat kesehatan. Terlepas dari kekhawatiran tentang kadar kolesterol, kopi dianggap aman
                            bagi kebanyakan orang. Tidak ada bahaya jangka panjang untuk minum kopi dalam jumlah sedang.
                            Kopi termasuk sumber antioksidan dan mikronutrien nabati lainnya yang dapat membantu
                            mencegah kanker, penyakit jantung, dan penurunan kognitif. Senyawa dalam kopi dapat membantu
                            mengurangi peradangan, memperbaiki pencernaan, dan meredakan gejala kesehatan mental seperti
                            depresi.
                        </p>
                        <p class="text-start">
                            Namun, terlalu banyak kopi atau sumber kafein apa pun dapat memiliki efek samping seperti
                            rasa gelisah, cemas, mual, jantung berdebar, dan dalam kasus ekstrem bisa membuat orang
                            tidak sadarkan diri.
                        </p>
