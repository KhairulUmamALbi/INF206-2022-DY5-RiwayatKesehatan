                        <h2 class="text-center">
                            <!-- Judul --> Manfaat Kunyit untuk Kesehatan Tubuh yang Tidak Boleh Kamu Lewatkan
                        </h2><br>
                        <div class="f1">
                            <author>Zakiy Marenda</author>
                        </div>
                        <div class="fr">
                            <time itemprop="datePublished" datetime="05 May 2022 | 21:19">Kamis, 05 Mei 2022 | 21:19
